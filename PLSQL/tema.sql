   log_time    timestamp default systimestamp,
   module_name varchar2(100),
   line_no     number,
   log_level   varchar2(10),
   log_message varchar2(4000),
   session_id  varchar2(50) default sys_context(
      'USERENV',
      'SESSIONID'
   )
);


---pachet debug
create or replace package debug_utils as
   g_debug_mode boolean := false;
   procedure enable_debug;
   procedure disable_debug;

   procedure log_msg (
      p_module_name in varchar2,
      p_line_no     in number,
      p_log_level   in varchar2,
      p_message     in varchar2
   );

   procedure log_variable (
      p_module_name in varchar2,
      p_line_no     in number,
      p_log_level   in varchar2,
      p_name        in varchar2,
      p_value       in varchar2
   );

   procedure log_error (
      p_module_name in varchar2,
      p_line_no     in number,
      p_log_level   in varchar2,
      p_error_msg   in varchar2
   );
end debug_utils;
/


---body pachet debug
create or replace package body debug_utils as

   procedure enable_debug is
   begin
      g_debug_mode := true;
   end enable_debug;

   procedure disable_debug is
   begin
      g_debug_mode := false;
   end disable_debug;

   procedure log_msg (
      p_module_name in varchar2,
      p_line_no     in number,
      p_log_level   in varchar2,
      p_message     in varchar2
   ) is
   begin
      if g_debug_mode then
         insert into debug_log (
            module_name,
            line_no,
            log_level,
            log_message
         ) values ( p_module_name,
                    p_line_no,
                    p_log_level,
                    p_message );
      end if;
   end log_msg;

   procedure log_variable (
      p_module_name in varchar2,
      p_line_no     in number,
      p_log_level   in varchar2,
      p_name        in varchar2,
      p_value       in varchar2
   ) is
   begin
      if g_debug_mode then
         insert into debug_log (
            module_name,
            line_no,
            log_level,
            log_message
         ) values ( p_module_name,
                    p_line_no,
                    p_log_level,
                    'Variable "'
                    || p_name
                    || '" = '
                    || p_value );
      end if;
   end log_variable;

   procedure log_error (
      p_module_name in varchar2,
      p_line_no     in number,
      p_log_level   in varchar2,
      p_error_msg   in varchar2
   ) is
   begin
      if g_debug_mode then
         insert into debug_log (
            module_name,
            line_no,
            log_level,
            log_message
         ) values ( p_module_name,
                    p_line_no,
                    p_log_level,
                    'ERROR: ' || p_error_msg );
      end if;
   end log_error;

end debug_utils;
/

