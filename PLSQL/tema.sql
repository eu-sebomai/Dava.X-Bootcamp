create or replace procedure adjust_salaries_by_commission is
---cursor pt parcurgere
   cursor c_employees is
   select employee_id,
          salary,
          commission_pct
     from employees;

   v_employee_id    employees.employee_id%type;
   v_salary         employees.salary%type;
   v_commission_pct employees.commission_pct%type;
   v_new_salary     employees.salary%type;
begin
   debug_utils.log_msg(
      'adjust_salaries_by_commission',
      10,
      'INFO',
      'Procedure started'
   );
   open c_employees;
   loop
    ---procesare logica
      fetch c_employees into
         v_employee_id,
         v_salary,
         v_commission_pct;
      exit when c_employees%notfound;
      debug_utils.log_msg(
         'adjust_salaries_by_commission',
         20,
         'INFO',
         'Processing employee_id = ' || v_employee_id
      );
      if v_commission_pct is not null then
         v_new_salary := v_salary + ( v_salary * v_commission_pct );
         debug_utils.log_msg(
            'adjust_salaries_by_commission',
            30,
            'INFO',
            'Commission found: ' || v_commission_pct
         );
      else
         v_new_salary := v_salary + ( v_salary * 0.02 );
         debug_utils.log_msg(
            'adjust_salaries_by_commission',
            40,
            'WARN',
            'Commission is NULL, default 2% applied'
         );
      end if;

      debug_utils.log_variable(
         'adjust_salaries_by_commission',
         45,
         'INFO',
         'v_new_salary',
         to_char(v_new_salary)
      );
      update employees
         set
         salary = v_new_salary
       where employee_id = v_employee_id;
      debug_utils.log_msg(
         'adjust_salaries_by_commission',
         50,
         'INFO',
         'Updated salary to ' || v_new_salary
      );
   end loop;

   close c_employees;
   debug_utils.log_msg(
      'adjust_salaries_by_commission',
      90,
      'INFO',
      'Procedure finished'
   );
   commit;
exception
   when others then
      debug_utils.log_error(
         'adjust_salaries_by_commission',
         99,
         'ERROR',
         sqlerrm
      );
      if c_employees%isopen then
         close c_employees;
      end if;
      rollback;
      raise;
end;
/
